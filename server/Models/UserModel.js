const mongoose = require("mongoose");

const Schema = mongoose.Schema;

const UserSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  gender: {
    type: String,
    required: true,
  },
  age: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: false,
    default: "User",
    enum: ["Admin", "SuperAdmin", "User"],
  },
});

module.exports = mongoose.model("User", UserSchema);
